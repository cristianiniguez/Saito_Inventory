    private void OcultarColumnas() {
        tab_stock.getColumnModel().getColumn(0).setMaxWidth(0);
        tab_stock.getColumnModel().getColumn(0).setMinWidth(0);
        tab_stock.getColumnModel().getColumn(0).setPreferredWidth(0);
    }
    
    private void Mostrar(String buscar) {
        try {
            DefaultTableModel modelo;
            ConsulStock func = new ConsulStock();
            modelo = func.mostrar(buscar);
            tab_stock.setModel(modelo);
            OcultarColumnas();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al mostrar stock");
            System.err.println("Error al mostrar stock: " + e);
        }
    }
    
    private void Informacion(String buscar) {
        try {
            HashMap info;
            ConsulStock func = new ConsulStock();
            info = func.info(buscar);
            lbl_compras.setText(info.get("Compras").toString());
            lbl_ventas.setText(info.get("Ventas").toString());
            lbl_utilidad.setText(info.get("Utilidad").toString());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al mostrar stock");
            System.err.println("Error al mostrar stock: " + e);
        }
    }

